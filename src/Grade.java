
public class Grade {
	private String id;
	private String grade;
	
	public Grade(String id, String grade){
		this.id = id;
		this.grade = grade;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getGrade() {
		return grade;
	}
	public void setGrade(String grade) {
		this.grade = grade;
	}
}
